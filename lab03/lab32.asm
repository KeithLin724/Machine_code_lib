
	ORG 0000H
	AJMP MAIN
	ORG 0050H
MAIN:
	MOV	DPTR,#SSTABLE;
	MOV	 R3,P1
	INC R3
	MOV P1,R3
LOOP:
	MOV	R2,#0;

LOOP1:
	MOV	A,R2
	MOVC	A,@A+DPTR
	MOV	P0,A
	INC	R2
	ACALL	DELAY
	CJNE	R2,#7,LOOP1
	AJMP	LOOP
SSTABLE:
	DB	0C0H	;  11000000B=0
	DB	082H	;  10000010B=6
	DB	0F9H	;  11111001B=1
	DB	0C0H	;  11000000B=0
	DB	080H	;  10000000B=8
	DB	0F8H	;  11111000B=7
	DB	0A4H	;  10100100B=2	
DELAY:
	MOV R5,P1;
DELAY1:
	MOV R6,#0FFH;
DELAY2:
	MOV R7,#40H;
DELAY3:
	DJNZ R7,DELAY3
	DJNZ R6,DELAY2
	DJNZ R5,DELAY1
	RET
	END
