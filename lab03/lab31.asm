
	ORG 0000H
	AJMP MAIN
	ORG 0050H
MAIN:
	MOV	DPTR,#SSTABLE;
LOOP:
	MOV	R2,#0;
LOOP1:
	MOV	A,R2
	MOVC	A,@A+DPTR
	MOV	P0,A
	INC	R2
	ACALL	DELAY
	CJNE	R2,#10,LOOP1
	AJMP	LOOP
SSTABLE:
	DB	0C0H	;  11000000B=0
	DB	0F9H	;  11111001B=1
	DB	0A4H	;  10100100B=2
	DB	0B0H	;  10110000B=3
	DB	099H	;  10011001B=4
	DB	092H	;  10010010B=5
	DB	082H	;  10000010B=6
	DB	0F8H	;  11111000B=7
	DB	080H	;  10000000B=8
	DB	090H	;  10010000B=9
DELAY:
	MOV R5,#0FFH;
DELAY1:
	MOV R6,#0FFH;
DELAY2:
	MOV R7,#05H;
DELAY3:
	DJNZ R7,DELAY3
	DJNZ R6,DELAY2
	DJNZ R5,DELAY1
	RET
	END

