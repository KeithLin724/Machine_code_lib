     ORG 0000H
     AJMP MAIN
     ORG 0050H
MAIN:
	MOV A,P0
	MOV R0,A
	MOV A,P3
	CPL A
	XRL A,R0
	MOV P2,A
	MOV R2 ,#0
	JMP LOOP1

LOOP1:
	JBC P2.0,LOOP
	JBC P2.1,LOOP
	JBC P2.2,LOOP
	JBC P2.3,LOOP
	JBC P2.4,LOOP
	JBC P2.5,LOOP
	JBC P2.6,LOOP
	JBC P2.7,LOOP

	MOV A,R2
	CPL A
	MOV P1,A

	JMP MAIN
LOOP:
	INC R2
	JMP LOOP1
